/**
 * Helper: Check if user has import privileges
 * Mirrors the backup privilege pattern from isBackupAllowed()
 */
function hasImportPrivilege(user) {
    if (!user) return false;
    if (user.globalRole === 'creator' || user.globalRole === 'it_admin') return true;
    // Check delegated import privilege list
    try {
        const row = logisticsDb.prepare('SELECT Value FROM SystemSettings WHERE Key = ?').get('allowed_import_users');
        if (!row) return false;
        const users = JSON.parse(row.Value);
        return Array.isArray(users) && users.includes(user.Username);
    } catch (e) {
        return false;
    }
}

/**
 * POST /api/database/reset-plant
 * 
 * Completely wipes all DATA rows from a plant database while preserving:
 * - Schema structure (all tables remain)
 * - Migration history (schema_version table)
 * - Lookup/reference tables (WorkType, WorkStatuses, Locations, Departments, 
 *   PartClasses, AssetTypes, CostCenters, Shifts, SiteLeadership, TaskTypes,
 *   PurchaseStatuses, WorkObj, ProcObj, PartObj, ObjType, Object)
 * 
 * TRIPLE-GATED SAFETY:
 *   Gate 1: Role check (Creator/IT Admin/delegated import user)
 *   Gate 2: Confirmation string (must type exact plant name)
 *   Gate 3: Mandatory pre-reset snapshot (non-skippable)
 */
router.post('/reset-plant', (req, res) => {
    try {
        // ── GATE 1: Role Check ──
        if (!hasImportPrivilege(req.user)) {
            return res.status(403).json({ 
                error: 'Import/Reset privileges required. Contact your IT Administrator.' 
            });
        }

        // ── GATE 2: Confirmation String ──
        const { confirmPlantName } = req.body;
        const plantId = db.asyncLocalStorage.getStore() || 'Demo_Plant_1';
        
        // Load plants.json to get the display label for the plant
        const plantsFile = path.join(dataDir, 'plants.json');
        let plantLabel = plantId;
        if (fs.existsSync(plantsFile)) {
            const plants = JSON.parse(fs.readFileSync(plantsFile, 'utf8'));
            const found = plants.find(p => p.id === plantId);
            if (found) plantLabel = found.label;
        }

        if (!confirmPlantName || (confirmPlantName !== plantId && confirmPlantName !== plantLabel)) {
            return res.status(400).json({ 
                error: `Safety check failed. You must type "${plantLabel}" to confirm reset.`,
                expectedConfirmation: plantLabel
            });
        }

        const connection = db.getDb();

        // ── GATE 3: Mandatory Pre-Reset Snapshot ──
        let dbFileName = `${plantId}.db`;
        if (plantId === 'Demo_Plant_1' && fs.existsSync(path.join(dataDir, 'Trier OS.db'))) {
            dbFileName = 'Trier OS.db';
        }
        const sourcePath = path.join(dataDir, dbFileName);
        const snapshotName = `${dbFileName}.RESET_SNAP_${Date.now()}`;
        const snapshotPath = path.join(dataDir, snapshotName);

        if (!fs.existsSync(sourcePath)) {
            return res.status(404).json({ error: 'Database file not found for this plant.' });
        }

        // Flush WAL and create snapshot
        connection.pragma('wal_checkpoint(TRUNCATE)');
        fs.copyFileSync(sourcePath, snapshotPath);
        console.log(`🛡️ Pre-Reset Snapshot created: ${snapshotName}`);

        // ── EXECUTE RESET ──
        // Tables to PRESERVE (lookup/reference data — no user data)
        const preserveTables = [
            'schema_version',        // Migration history
            'WorkType',              // PM, FSP, Corrective, Emergency, etc.
            'WorkStatuses',          // Open, In Progress, Complete, etc.
            'TaskTypes',             // Task type definitions
            'PurchaseStatuses',      // PO status definitions
            'AssetTypes',            // Asset category definitions
            'PartClasses',           // Part classification definitions
            'CostCenters',           // Cost center definitions
            'Shifts',                // Shift definitions
            'WorkObj', 'ProcObj', 'PartObj', 'ObjType', 'Object', // System object defs
            'zAddOn', 'zAddrCst', 'zAllComp', 'zAppInfo',         // PMC system tables
            'zCompanyVer', 'zDatabaseObjectTypePermissions',
            'zDatabaseObjectTypes', 'zDatabaseObjects',
            'zDatabaseObjectsGroups', 'zEntity', 'zErrLog',
            'zForm', 'zGroups', 'zMessage', 'zPPP', 'zPartCst',
            'zPermissionTypes', 'zPrjStat', 'zReport', 'zReqNotify',
            'zReqStatus', 'zRptcls', 'zSchType', 'zUpHist',
            'zUserCo', 'zUserGroups', 'zUserPOStat', 'zUserWOStat'
        ];

        // Tables to WIPE (all user/operational data)
        const wipeTables = [
            'Work', 'Asset', 'Part', 'Schedule', 'Vendors', 'Procedures',
            'Task', 'TaskStep',
            'WorkParts', 'WorkLabor', 'WorkNote', 'WorkMisc', 'WorkTask',
            'WorkRels', 'WorkObj',
            'PartAdjustments', 'PartVendors', 'PartLocations', 'PartOrderRules',
            'ProcedureParts', 'ProcedureTasks',
            'Locations', 'Departments', 'SiteLeadership',
            'Users', 'SystemUsers',
            'Calendar', 'Company', 'MsgCfgShare', 'MsgStd', 'Project'
        ];

        let tablesWiped = 0;
        let totalRowsDeleted = 0;

        const resetTransaction = connection.transaction(() => {
            for (const table of wipeTables) {
                try {
                    // Check if table exists before trying to delete
                    const exists = connection.prepare(
                        "SELECT name FROM sqlite_master WHERE type='table' AND name=?"
                    ).get(table);
                    
                    if (exists) {
                        const countBefore = connection.prepare(`SELECT COUNT(*) as c FROM "${table}"`).get().c;
 /* dynamic col/table - sanitize inputs */
                        if (countBefore > 0) {
                            connection.prepare(`DELETE FROM "${table}"`).run();
 /* dynamic col/table - sanitize inputs */
                            totalRowsDeleted += countBefore;
                            tablesWiped++;
                            console.log(`  🗑️ Wiped ${table}: ${countBefore} rows deleted`);
                        }
                    }
                } catch (tableErr) {
                    console.warn(`  ⚠️ Could not wipe ${table}: ${tableErr.message}`);
                }
            }
        });

        resetTransaction();

        // Log the critical event
        logAudit(
            req.user?.Username || 'SYSTEM',
            'PLANT_RESET',
            plantId,
            { 
                tablesWiped, 
                totalRowsDeleted, 
                snapshotFile: snapshotName,
                confirmedBy: req.user?.Username
            },
            'CRITICAL',
            req.ip
        );

        console.log(`\n🔴 PLANT RESET COMPLETE: ${plantId}`);
        console.log(`   Tables wiped: ${tablesWiped}`);
        console.log(`   Rows deleted: ${totalRowsDeleted}`);
        console.log(`   Snapshot: ${snapshotName}\n`);

        res.json({
            success: true,
            plant: plantId,
            plantLabel,
            tablesWiped,
            totalRowsDeleted,
            snapshotFile: snapshotName,
            message: `Plant database reset complete. ${totalRowsDeleted} rows removed from ${tablesWiped} tables. Snapshot saved as ${snapshotName}.`
        });
    } catch (err) {
        console.error('❌ Plant reset failed:', err);
        res.status(500).json({ error: 'Plant reset failed: ' });
    }
});

