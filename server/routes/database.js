/**
 * Trier OS — Database Administration API
 * =========================================
 * Administrative endpoints for database inspection, backup, restore,
 * snapshot management, and plant registration. Restricted to IT Admin
 * and authorized Backup Operator roles.
 * Mounted at /api/database in server/index.js.
 *
 * ENDPOINTS:
 *   GET    /plants                     List all registered plant databases with size and last modified
 *   PUT    /plants                     Register or update plant database metadata
 *   POST   /backup                     Trigger an immediate backup of the current plant DB
 *                                      Returns: { snapshotPath, size, checksum }
 *   GET    /export                     Download current plant DB as a SQLite binary file
 *                                      Supports: ?format=sqlite (default) | ?format=json (table dump)
 *   GET    /snapshots                  List available backup snapshots for the current plant
 *   POST   /restore                    Restore a plant DB from a named snapshot
 *                                      CAUTION: Overwrites live database — requires confirmation token
 *   GET    /privileges/backup          Check if current user has backup operator privilege
 *   POST   /privileges/backup          Grant or revoke backup operator privilege for a user
 *   POST   /import                     Import a SQLite database file into a plant slot
 *   POST   /reset-plant                Factory-reset a plant DB to the schema template
 *                                      CAUTION: Destroys all plant data — requires double confirmation
 *   GET    /privileges/import          Check if current user has import privilege
 *   POST   /privileges/import          Grant or revoke import privilege for a user
 *   POST   /cleanup-snapshots          Remove snapshots older than the retention policy (default: 30 days)
 *
 * BACKUP STRATEGY: Snapshots are SQLite hot-backup copies (WAL checkpoint + file copy).
 *   Stored in /data/backups/{plantId}/{timestamp}.db.
 *   Checksum (SHA-256) is recorded so restore integrity can be verified.
 *
 * RESTORE SAFETY: /restore requires a confirmation token generated by a prior admin action.
 *   Audit log entry is written to logistics_db before and after every restore operation.
 *
 * RESET SAFETY: /reset-plant requires two separate confirmation codes (one per request).
 *   The operation copies schema_template.db over the target plant file.
 *   Cannot be performed on the currently active plant context.
 */
