// Save updated list of plants — auto-creates DB for new plants
router.put('/plants', (req, res) => {
    try {
        if (!req.user || (req.user.role !== 'it_admin' && req.user.globalRole !== 'it_admin' && req.user.globalRole !== 'creator')) {
            return res.status(403).json({ error: 'Administrative privileges required to modify locations.' });
        }

        const newPlants = req.body;
        if (!Array.isArray(newPlants)) {
            return res.status(400).json({ error: 'Expected an array' });
        }

        // Load existing plants to detect NEW ones
        const plantsFile = path.join(dataDir, 'plants.json');
        let existingPlants = [];
        if (fs.existsSync(plantsFile)) {
            existingPlants = JSON.parse(fs.readFileSync(plantsFile, 'utf8'));
        }
        const existingIds = new Set(existingPlants.map(p => p.id));

        // Relies exclusively on the master blueprint database so production nodes aren't linked
        const templatePath = fs.existsSync(path.join(dataDir, 'schema_template.db'))
            ? path.join(dataDir, 'schema_template.db')
            : (fs.existsSync(path.join(dataDir, 'Demo_Plant_1.db')) ? path.join(dataDir, 'Demo_Plant_1.db') : path.join(dataDir, 'schema_template.sqlite'));
            
        if (!fs.existsSync(templatePath)) {
            return res.status(500).json({ error: 'Master Schema Template DB not found. Ensure schema_template.db is in /data.' });
        }

        const created = [];
        for (const plant of newPlants) {
            if (!existingIds.has(plant.id)) {
                // New plant — create empty DB with schema from template
                const newDbPath = path.join(dataDir, `${plant.id}.db`);
                if (!fs.existsSync(newDbPath) && templatePath) {
                    try {
                        const Database = require('better-sqlite3');
                        const templateDb = new Database(templatePath, { readonly: true });

                        // Get all table creation SQL from the template
                        const tables = templateDb.prepare(
                            "SELECT sql FROM sqlite_master WHERE type='table' AND sql IS NOT NULL"
                        ).all();

                        // Get all index creation SQL
                        const indexes = templateDb.prepare(
                            "SELECT sql FROM sqlite_master WHERE type='index' AND sql IS NOT NULL"
                        ).all();

                        templateDb.close();

                        // Create the new DB and run all CREATE TABLE statements
                        const newDb = new Database(newDbPath);
                        newDb.pragma('journal_mode = WAL');

                        for (const t of tables) {
                            try {
                                newDb.exec(t.sql);
                            } catch (e) {
                                // Skip tables that conflict (e.g., sqlite_sequence)
                            }
                        }

                        // Create indexes
                        for (const idx of indexes) {
                            try {
                                newDb.exec(idx.sql);
                            } catch (e) { /* Intentional: skip conflicting indexes (e.g., sqlite_sequence) */ }
                        }

                        newDb.close();

                        // Seed initial PlantConfiguration row in logistics DB
                        try {
                            logisticsDb.prepare(`
                                INSERT OR IGNORE INTO PlantConfiguration (PlantID, ProductionModel, HubIp, UpdatedAt)
                                VALUES (?, 'fluid-process', ?, datetime('now'))
                            `).run(plant.id, plant.hubIp || null);
                        } catch (_) {}

                        created.push(plant.id);
                        console.log(`✅ Created new plant database: ${plant.id}.db (schema copied from Demo Plant 1)`);
                    } catch (dbErr) {
                        console.error(`❌ Failed to create DB for ${plant.id}:`, dbErr.message);
                    }
                }
            }
        }

        // Detect DELETED plants by diffing incoming vs existing
        const newIds = new Set(newPlants.map(p => p.id));
        const deletedPlants = existingPlants.filter(p => !newIds.has(p.id));
        
        for (const dp of deletedPlants) {
            const dpId = dp.id;
            if (dpId === 'Corporate_Office' || dpId === 'examples') {
                console.warn(`⚠️ Skipped protected location deletion request for: ${dpId}`);
                continue;
            }
            console.log(`🗑️ UI requested deletion of location: ${dpId}`);
            try {
                logisticsDb.prepare('DELETE FROM safety_incidents WHERE PlantID = ?').run(dpId);
                logisticsDb.prepare('DELETE FROM SafetyPermits WHERE PlantID = ?').run(dpId);
                logisticsDb.prepare('DELETE FROM LotoPermits WHERE PlantID = ?').run(dpId);
                logisticsDb.prepare('DELETE FROM calibration_instruments WHERE PlantID = ?').run(dpId);
                if(logisticsDb.prepare("SELECT name FROM sqlite_master WHERE type='table' AND name='compliance_inspections'").get()) {
                    logisticsDb.prepare('DELETE FROM compliance_inspections WHERE PlantID = ?').run(dpId);
                }
            } catch(e) { console.warn(`Could not wipe metadata for ${dpId}:`, e.message); }

            const dbPath = path.join(dataDir, `${dpId}.db`);
            try { if(fs.existsSync(dbPath)) fs.unlinkSync(dbPath); } catch(e) { console.warn('DB file locked; purge on reboot.'); }
            try { if(fs.existsSync(dbPath+'-wal')) fs.unlinkSync(dbPath+'-wal'); } catch(e){}
            try { if(fs.existsSync(dbPath+'-shm')) fs.unlinkSync(dbPath+'-shm'); } catch(e){}
        }

        const sorted = newPlants.sort((a, b) => a.label.localeCompare(b.label));
        fs.writeFileSync(plantsFile, JSON.stringify(sorted, null, 2));

        logAudit(req.user?.username || req.user?.Username || 'admin', 'LOCATIONS_UPDATE', 'all_sites', 
            { count: sorted.length, newDatabases: created }, 'INFO', req.ip);

        res.json({ success: true, count: sorted.length, newDatabases: created });
    } catch (err) {
        console.error('Failed to save plants:', err);
        res.status(500).json({ error: 'Failed' });
    }
});

// Delete a plant entirely
router.delete('/plants/:id', (req, res) => {
    try {
        if (!req.user || (req.user.role !== 'it_admin' && req.user.globalRole !== 'it_admin' && req.user.globalRole !== 'creator')) {
            return res.status(403).json({ error: 'Administrative privileges required.' });
        }

        const targetPlantId = req.params.id;
        if (!targetPlantId || targetPlantId === 'schema_template') {
            return res.status(400).json({ error: 'Invalid plant ID.' });
        }

        const plantsFile = path.join(dataDir, 'plants.json');
        if (fs.existsSync(plantsFile)) {
            let plants = JSON.parse(fs.readFileSync(plantsFile, 'utf8'));
            plants = plants.filter(p => p.id !== targetPlantId);
            fs.writeFileSync(plantsFile, JSON.stringify(plants, null, 2));
        }

        // Wipe global logistics aggregates
        try {
            logisticsDb.prepare('DELETE FROM safety_incidents WHERE PlantID = ?').run(targetPlantId);
            logisticsDb.prepare('DELETE FROM SafetyPermits WHERE PlantID = ?').run(targetPlantId);
            logisticsDb.prepare('DELETE FROM LotoPermits WHERE PlantID = ?').run(targetPlantId);
            logisticsDb.prepare('DELETE FROM calibration_instruments WHERE PlantID = ?').run(targetPlantId);
            // Catch if check doesn't exist
            if(logisticsDb.prepare("SELECT name FROM sqlite_master WHERE type='table' AND name='compliance_inspections'").get()) {
                logisticsDb.prepare('DELETE FROM compliance_inspections WHERE PlantID = ?').run(targetPlantId);
            }
        } catch(e) { console.warn('Could not wipe logistics metadata for deleted plant:', e.message); }

        const dbPath = path.join(dataDir, `${targetPlantId}.db`);
        try { if(fs.existsSync(dbPath)) fs.unlinkSync(dbPath); } catch(e) { console.warn('DB file locked; will purge on next reboot.'); }
        try { if(fs.existsSync(dbPath+'-wal')) fs.unlinkSync(dbPath+'-wal'); } catch(e){}
        try { if(fs.existsSync(dbPath+'-shm')) fs.unlinkSync(dbPath+'-shm'); } catch(e){}

        logAudit(req.user?.Username || 'SYSTEM', 'LOCATIONS_DELETE', targetPlantId, {}, 'CRITICAL', req.ip);

        res.json({ success: true, message: `Plant ${targetPlantId} successfully purged.` });
    } catch (err) {
        console.error('Failed to delete plant:', err);
        res.status(500).json({ error: 'Failed to delete plant' });
    }
});

// Database backup
router.post('/backup', (req, res) => {
    try {
        const activeUserRole = req.user.globalRole;
        const isCreator = req.user.globalRole === 'creator';
        const username = req.user.Username;

        const inBackupList = username ? isBackupAllowed(username) : false;

        if (activeUserRole !== 'it_admin' && !isCreator && !inBackupList) {
            return res.status(403).json({ error: 'Administrative privileges required for deep backup.' });
        }

        const plantId = db.asyncLocalStorage.getStore() || 'Demo_Plant_1';
        let dbFileName = `${plantId}.db`;
        if (plantId === 'Demo_Plant_1' && fs.existsSync(path.join(dataDir, 'Trier OS.db'))) {
            dbFileName = 'Trier OS.db';
        }

        const sourcePath = path.join(dataDir, dbFileName);
        const backupName = `${dbFileName}.bak_${Date.now()}`;
        const targetPath = path.join(dataDir, backupName);

        if (!fs.existsSync(sourcePath)) {
            return res.status(404).json({ error: 'Database not found for this location' });
        }

        // Ensure WAL is flushed before backing up
        const connection = db.getDb();
        connection.pragma('wal_checkpoint(TRUNCATE)');

        fs.copyFileSync(sourcePath, targetPath);

        logAudit(req.user?.Username || 'SYSTEM', 'DB_BACKUP', plantId, { backupName }, 'INFO', req.ip);

        res.json({ success: true, message: `Backup created: ${backupName}`, file: backupName });
    } catch (err) {
        console.error('Failed to backup:', err);
        res.status(500).json({ error: 'Backup failed' });
    }
});

