// Restore a snapshot
router.post('/restore', (req, res) => {
    try {
        const { snapshotFile } = req.body;
        const activeUserRole = req.user.globalRole;
        const isCreator = req.user.globalRole === 'creator';

        if (activeUserRole !== 'it_admin' && !isCreator) {
            return res.status(403).json({ error: 'Administrative privileges required for database restoration.' });
        }

        if (!snapshotFile) return res.status(400).json({ error: 'No snapshot specified' });

        const safeSnapshotFile = path.basename(snapshotFile);
        const snapshotPath = path.join(dataDir, safeSnapshotFile);
        
        // Boundary Check for LFI
        if (!path.resolve(snapshotPath).startsWith(path.resolve(dataDir))) {
            return res.status(403).json({ error: 'Boundary escape detected.' });
        }

        if (!fs.existsSync(snapshotPath)) return res.status(404).json({ error: 'Snapshot not found' });

        // Determine target DB
        const plantId = db.asyncLocalStorage.getStore() || 'Demo_Plant_1';
        let dbFileName = `${plantId}.db`;
        if (plantId === 'Demo_Plant_1' && fs.existsSync(path.join(dataDir, 'Trier OS.db'))) {
            dbFileName = 'Trier OS.db';
        }
        const targetPath = path.join(dataDir, dbFileName);

        // Ensure WAL is flushed and connection is temporarily handled
        const connection = db.getDb();
        connection.pragma('wal_checkpoint(TRUNCATE)');
        
        // In a real-world high-traffic app, we should probably close connections here, 
        // but for SQLite single-file swap, copy-over usually works if WAL is flushed.
        fs.copyFileSync(snapshotPath, targetPath);

        logAudit(req.user?.username || 'it_admin', 'DB_RESTORE', plantId, { from: snapshotFile }, 'CRITICAL', req.ip);

        res.json({ success: true, message: `System restored to snapshot: ${snapshotFile}` });
    } catch (err) {
        console.error('Restore failed:', err);
        res.status(500).json({ error: 'Restore failed' });
    }
});

