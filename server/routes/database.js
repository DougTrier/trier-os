// Snapshot Cleanup — 30 day auto-cleanup of old snapshots
// ═══════════════════════════════════════════════════════════════════════════════

router.post('/cleanup-snapshots', (req, res) => {
    try {
        if (!req.user || (req.user.globalRole !== 'it_admin' && req.user.globalRole !== 'creator')) {
            return res.status(403).json({ error: 'Administrative privileges required.' });
        }

        const THIRTY_DAYS_MS = 30 * 24 * 60 * 60 * 1000;
        const now = Date.now();
        const files = fs.readdirSync(dataDir);
        let cleaned = 0;
        const cleanedFiles = [];

        for (const file of files) {
            if (file.includes('.IMPORT_SNAP_') || file.includes('.RESET_SNAP_') || file.includes('.bak_')) {
                const filePath = path.join(dataDir, file);
                const stats = fs.statSync(filePath);
                const ageMs = now - stats.mtimeMs;

                if (ageMs > THIRTY_DAYS_MS) {
                    fs.unlinkSync(filePath);
                    cleaned++;
                    cleanedFiles.push(file);
                    console.log(`🧹 Cleaned old snapshot: ${file}`);
                }
            }
        }

        logAudit(req.user?.Username || 'SYSTEM', 'SNAPSHOT_CLEANUP', 'all_sites', { cleaned, files: cleanedFiles }, 'INFO', req.ip);

        res.json({ success: true, cleaned, files: cleanedFiles });
    } catch (err) {
        console.error('Snapshot cleanup failed:', err);
        res.status(500).json({ error: 'Cleanup failed' });
    }
});

